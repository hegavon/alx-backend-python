#!/usr/bin/env python3
"""
This module provides a function `floor` that takes a float and returns its
floor value.
"""

import math


def floor(n: float) -> int:
    """
    Returns the floor of the float `n`.

    Parameters:
    n (float): The number to floor.

    Returns:
    int: The floor value of `n`.
    """
    return math.floor(n)
