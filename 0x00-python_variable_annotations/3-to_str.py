#!/usr/bin/env python3
"""
This module provides a function `to_str` that takes a float and returns its
string representation.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of the float `n`.

    Parameters:
    n (float): The number to convert to a string.

    Returns:
    str: The string representation of `n`.
    """
    return str(n)
